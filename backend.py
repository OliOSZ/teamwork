def leggSammen():
    tall1 = input("Skriv in første tallet: ")
    tall2 = input("Skriv in andre tallet: ")
    sum = int(tall1) + int(tall2)
    print(f'{tall1} + {tall2} = {sum}')
    
    
def trekkFra():
    tall1 = input("Skriv in første tallet: ")
    tall2 = input("Skriv in andre tallet: ")
    diff = int(tall1) - int(tall2)
    print(f'{tall1} - {tall2} = {diff}')
    